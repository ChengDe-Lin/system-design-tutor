import { useState, useMemo, useEffect, useRef, useCallback } from 'react'
import ReactMarkdown from 'react-markdown'
import remarkGfm from 'remark-gfm'
import rehypeRaw from 'rehype-raw'
import mermaid from 'mermaid'
import type { Components } from 'react-markdown'

// ─── Mermaid Setup ───

mermaid.initialize({
  startOnLoad: false,
  theme: 'dark',
  fontFamily: 'Plus Jakarta Sans, sans-serif',
})

function MermaidDiagram({ chart }: { chart: string }) {
  const [svg, setSvg] = useState('')

  useEffect(() => {
    const id = `m-${Date.now()}-${Math.random().toString(36).slice(2)}`
    // Content is from local markdown files only — trusted source
    mermaid
      .render(id, chart)
      .then(({ svg: renderedSvg }) => setSvg(renderedSvg))
      .catch(() => setSvg(''))
  }, [chart])

  if (!svg) return <pre><code>{chart}</code></pre>
  // Safe: SVG is generated by mermaid from our own local markdown content
  return <div className="mermaid-diagram" dangerouslySetInnerHTML={{ __html: svg }} />
}

// ─── Types ───

interface FileEntry {
  path: string
  name: string
  content: string
}

interface Section {
  key: string
  title: string
  description: string
  color: string
  files: FileEntry[]
}

interface Heading {
  level: number
  text: string
  id: string
}

